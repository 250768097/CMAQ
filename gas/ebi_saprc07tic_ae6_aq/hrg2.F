      SUBROUTINE HRG2( DTC )
C**********************************************************************
C
C  FUNCTION: To solve for the concentration of HO, HO2, HONO, and
C            HNO4 alebraically.
C
C  PRECONDITIONS: For the SAPRC07TIC family of mechanisms
C
C  KEY SUBROUTINES/FUNCTIONS CALLED: None
C
C  REVISION HISTORY: Created by EBI solver program, July 18, 2014
C
C   18 Jul 14 B.Hutzell: revised to use real(8) variables
C**********************************************************************
      USE HRDATA

      IMPLICIT NONE


C..INCLUDES: None


C..ARGUMENTS:
      REAL( 8 ), INTENT( IN ) :: DTC                      ! Time step

C..PARAMETERS: None


C..EXTERNAL FUNCTIONS: NONE


C..SAVED LOCAL VARIABLES:
      CHARACTER( 16 ), SAVE :: PNAME = 'HRG2'             ! Program name


C..SCRATCH LOCAL VARIABLES:
      REAL( 8 ) ::    O1D_S                 ! sum of O1D loss frequencies
      REAL( 8 ) ::    OH_S                  ! stoich coeff for OH from O1D+H2O or H2
      REAL( 8 ) ::    HO2_S                 ! stoich coeff for HO2 from O1D+H2 rxn
      REAL( 8 ) ::    EXN_S                 ! sum of NO2EX loss frequencies
      REAL( 8 ) ::    XOH_S                 ! stoich coeff for OH & HONO from NO2EX loss rxn
      REAL( 8 ) ::    R4_19                 ! production of OH from HONO
      REAL( 8 ) ::    R19_4                 ! production of HONO from OH
      REAL( 8 ) ::    R4_5                  ! production of OH from HO2
      REAL( 8 ) ::    R5_4                  ! production of HO2 from OH
      REAL( 8 ) ::    R5_21                 ! production of HO2 from HNO4
      REAL( 8 ) ::    R21_5                 ! production of HNO4 from HO2
      REAL( 8 ) ::    P4, P5, P19, P21      ! species production form other paths
      REAL( 8 ) ::    L4, L5, L19, L21      ! species loss terms

      REAL( 8 ) ::    A, B, C               ! coeffs of quadratic eq. for HO2
      REAL( 8 ) ::    Q                     ! intermediate term

      REAL( 8 ) ::    T1, T2, T3            ! intermediate terms

      REAL( 8 ) ::    L21_INV               ! reciprocal of HNO4 loss term

C**********************************************************************


c..stoichiometric coefficient for production of HOX from O3 via O1D
      O1D_S =                 RKI( NCELL,  20 )                            ! O1D=0.2000D+01*OH
     &      +                 RKI( NCELL,  21 )                            ! O1D=O3P

      OH_S =    2.0000D+00 * RKI( NCELL,  20 )                            ! O1D=0.2000D+01*OH

      OH_S  = OH_S / O1D_S


c..Production of HO from HO2 (r4,5 terms )
      R4_5 =                 RKI( NCELL,  31 ) * YC( NCELL, NO               )   ! HO2+NO=OH+NO2
     &     +                 RKI( NCELL,  36 ) * YC( NCELL, O3               )   ! HO2+O3=OH
     &     +    8.0000D-01 * RKI( NCELL,  39 ) * YC( NCELL, NO3              )   ! HO2+NO3=0.8000D+...
     &     +                 RKI( NCELL, 561 ) * YC( NCELL, CL               )   ! HO2+CL=OH+CLO
     &     +    1.2000D-01 * RKI( NCELL, 674 ) * YC( NCELL, ISOPO2           )   ! HO2+ISOPO2=...
     &     +    1.7500D-01 * RKI( NCELL, 777 ) * YC( NCELL, IEPOXOO          )   ! HO2+IEPOXOO=...

      R4_5  = R4_5 * DTC

c..Production of HO from HONO (r4,19 terms )
      R4_19 =                 RKI( NCELL,  23 )                            ! HONO=OH+NO

      R4_19 = R4_19 * DTC

c..Remaining HO production
      P4 =   OH_S  *       RXRAT( NCELL,  18 )      ! O1D=0.2000D+01*OH
     &   +                 RXRAT( NCELL,  28 )      ! HNO3=OH+NO2
     &   +    3.9000D-01 * RXRAT( NCELL,  34 )      ! HNO4=0.3900D+00*OH+0.6100D+...
     &   +    2.0000D+00 * RXRAT( NCELL,  41 )      ! HO2H=0.2000D+01*OH
     &   +                 RXRAT( NCELL,  49 )      ! NO2EX=OH+HONO
     &   +                 RXRAT( NCELL, 118 )      ! xOH+NO=OH+NO
     &   +                 RXRAT( NCELL, 120 )      ! xOH+NO3=OH+NO3
     &   +    5.0000D-01 * RXRAT( NCELL, 121 )      ! xOH+MEO2=0.5000D+00*OH+MEO2
     &   +    5.0000D-01 * RXRAT( NCELL, 122 )      ! xOH+RO2C=0.5000D+00*OH+RO2C
     &   +    5.0000D-01 * RXRAT( NCELL, 123 )      ! xOH+RO2XC=0.5000D+00*OH+RO2XC
     &   +                 RXRAT( NCELL, 124 )      ! xOH+MECO3=OH+MECO3
     &   +                 RXRAT( NCELL, 125 )      ! xOH+RCO3=OH+RCO3
     &   +                 RXRAT( NCELL, 126 )      ! xOH+BZCO3=OH+BZCO3
     &   +                 RXRAT( NCELL, 127 )      ! xOH+MACO3=OH+MACO3
     &   +                 RXRAT( NCELL, 217 )      ! COOH=OH+HO2+HCHO
     &   +                 RXRAT( NCELL, 219 )      ! ROOH=OH+HO2+RCHO
     &   +                 RXRAT( NCELL, 221 )      ! R6OOH=OH+0.1420D+00*HO2+...
     &   +                 RXRAT( NCELL, 223 )      ! RAOOH=OH+HO2+0.5000D+...
     &   +    8.2600D-01 * RXRAT( NCELL, 241 )      ! AFG1+O3=0.8260D+00*OH+...
     &   +    8.2600D-01 * RXRAT( NCELL, 244 )      ! AFG2+O3=0.8260D+00*OH+...
     &   +    4.7100D-01 * RXRAT( NCELL, 247 )      ! AFG3+O3=0.4710D+00*OH+...
     &   +    2.0800D-01 * RXRAT( NCELL, 248 )      ! MACR+O3=0.2080D+00*OH+...
     &   +    1.6400D-01 * RXRAT( NCELL, 250 )      ! MVK+O3=0.1640D+00*OH+...
     &   +    2.8500D-01 * RXRAT( NCELL, 254 )      ! IPRD+O3=0.2850D+00*OH+...
     &   +    3.3000D-01 * RXRAT( NCELL, 264 )      ! ACROLEIN+O3=0.3300D+00*OH+...
     &   +    1.7800D-01 * RXRAT( NCELL, 267 )      ! ACROLEIN=0.1780D+00*OH+...
     &   +                 RXRAT( NCELL, 269 )      ! CCOOOH=OH+CO2+MEO2
     &   +                 RXRAT( NCELL, 271 )      ! RCOOOH=OH+xCCHO+yROOH+CO2+xHO2
     &   +    1.6000D-01 * RXRAT( NCELL, 504 )      ! ETHENE+O3=0.1600D+00*OH+...
     &   +    3.5000D-01 * RXRAT( NCELL, 508 )      ! PROPENE+O3=0.3500D+00*OH+...
     &   +    8.0000D-02 * RXRAT( NCELL, 512 )      ! BUTADIENE13+O3=0.8000D-...
     &   +    2.6600D-01 * RXRAT( NCELL, 515 )      ! ISOPRENE+O3=0.2660D+00*OH+...
     &   +    7.2800D-01 * RXRAT( NCELL, 518 )      ! APIN+O3=0.7280D+00*OH+...
     &   +    5.0000D-01 * RXRAT( NCELL, 522 )      ! ACETYLENE+O3=0.5000D+00*OH+...
     &   +    1.2800D-01 * RXRAT( NCELL, 536 )      ! OLE1+O3=0.1280D+00*OH+...
     &   +    4.4300D-01 * RXRAT( NCELL, 540 )      ! OLE2+O3=0.4430D+00*OH+...
     &   +    4.9900D-01 * RXRAT( NCELL, 546 )      ! TERP+O3=0.4990D+00*OH+...
     &   +    4.9900D-01 * RXRAT( NCELL, 550 )      ! SESQ+O3=0.4990D+00*OH+...
     &   +                 RXRAT( NCELL, 571 )      ! HOCL=OH+CL
     &   +    4.1400D-01 * RXRAT( NCELL, 587 )      ! ROOH+CL=0.4140D+00*OH+HCL+...
     &   +    1.4500D-01 * RXRAT( NCELL, 588 )      ! R6OOH+CL=0.1450D+00*OH+HCL+...
     &   +    1.3900D-01 * RXRAT( NCELL, 589 )      ! RAOOH+CL=0.1390D+00*OH+...
     &   +                 RXRAT( NCELL, 680 )      ! HPALD=OH+HO2+0.5000D+...
     &   +    7.9000D-01 * RXRAT( NCELL, 696 )      ! HC5+O3=0.7900D+00*OH+...
     &   +    7.3000D-01 * RXRAT( NCELL, 703 )      ! ISOPND+O3=0.7300D+00*OH+...
     &   +    3.4000D-01 * RXRAT( NCELL, 710 )      ! ISOPNB+O3=0.3400D+00*OH+...
     &   +    8.5000D-01 * RXRAT( NCELL, 725 )      ! NIT1+O3=0.8500D+00*OH+...
     &   +    9.5000D-01 * RXRAT( NCELL, 777 )      ! HO2+IEPOXOO=0.1125D+01*OH+...
     &   +    1.2500D-01 * RXRAT( NCELL, 778 )      ! IEPOXOO+NO=0.1250D+00*OH+...
     &   +    6.3000D-02 * RXRAT( NCELL, 779 )      ! IEPOXOO+MEO2=0.6300D-01*OH+...
     &   +    6.3000D-02 * RXRAT( NCELL, 780 )      ! IEPOXOO+RO2C=0.6300D-01*OH+...
     &   +    1.2500D-01 * RXRAT( NCELL, 781 )      ! IEPOXOO+MECO3=0.1250D+...
