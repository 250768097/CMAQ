      R5_4 =                 RKI( NCELL,  26 ) * YC( NCELL, NO3              )   ! OH+NO3=HO2+NO2
     &     +                 RKI( NCELL,  29 ) * YC( NCELL, CO               )   ! OH+CO=HO2+CO2
     &     +                 RKI( NCELL,  30 ) * YC( NCELL, O3               )   ! OH+O3=HO2
     &     +                 RKI( NCELL,  42 ) * YC( NCELL, HO2H             )   ! OH+HO2H=HO2
     &     +                 RKI( NCELL,  44 ) * YC( NCELL, SO2              )   ! OH+SO2=HO2+SULF+...
     &     +                 RKI( NCELL,  45 )                            ! OH=HO2
     &     +                 RKI( NCELL, 200 ) * YC( NCELL, HCHO             )   ! OH+HCHO=HO2+CO
     &     +                 RKI( NCELL, 212 ) * YC( NCELL, MEOH             )   ! OH+MEOH=HO2+HCHO
     &     +                 RKI( NCELL, 213 ) * YC( NCELL, HCOOH            )   ! OH+HCOOH=HO2+CO2
     &     +    1.4800D-01 * RKI( NCELL, 222 ) * YC( NCELL, RAOOH            )   ! OH+RAOOH=0.1480D+...
     &     +    6.3000D-01 * RKI( NCELL, 226 ) * YC( NCELL, GLY              )   ! OH+GLY=0.6300D+...
     &     +    4.7200D-01 * RKI( NCELL, 257 ) * YC( NCELL, PRD2             )   ! OH+PRD2=0.4720D+...
     &     +    1.8900D-01 * RKI( NCELL, 259 ) * YC( NCELL, RNO3             )   ! OH+RNO3=0.1890D+...
     &     +    3.0000D-01 * RKI( NCELL, 521 ) * YC( NCELL, ACETYLENE        )   ! OH+ACETYLENE=...
     &     +    5.7000D-01 * RKI( NCELL, 523 ) * YC( NCELL, BENZENE          )   ! OH+BENZENE=...
     &     +    1.8100D-01 * RKI( NCELL, 524 ) * YC( NCELL, TOLUENE          )   ! OH+TOLUENE=...
     &     +    1.5900D-01 * RKI( NCELL, 525 ) * YC( NCELL, MXYL             )   ! OH+MXYL=0.1590D+...
     &     +    1.6100D-01 * RKI( NCELL, 526 ) * YC( NCELL, OXYL             )   ! OH+OXYL=0.1610D+...
     &     +    1.5900D-01 * RKI( NCELL, 527 ) * YC( NCELL, PXYL             )   ! OH+PXYL=0.1590D+...
     &     +    2.2000D-02 * RKI( NCELL, 528 ) * YC( NCELL, TRIMETH_BENZ124  )   ! OH+...
     &     +    9.5000D-01 * RKI( NCELL, 529 ) * YC( NCELL, ETOH             )   ! OH+ETOH=0.9500D+...
     &     +    1.2300D-01 * RKI( NCELL, 543 ) * YC( NCELL, ARO1             )   ! OH+ARO1=0.1230D+...
     &     +    7.7000D-02 * RKI( NCELL, 544 ) * YC( NCELL, ARO2             )   ! OH+ARO2=0.7700D-...
     &     +    7.5000D-01 * RKI( NCELL, 759 ) * YC( NCELL, HOCCHO           )   ! OH+HOCCHO=...
     &     +    8.2500D-01 * RKI( NCELL, 760 ) * YC( NCELL, HACET            )   ! OH+HACET=0.8250D+...
     &     +    5.0000D-01 * RKI( NCELL, 772 ) * YC( NCELL, DHMOB            )   ! OH+DHMOB=0.5000D+...
     &     +                 RKI( NCELL, 783 ) * YC( NCELL, RNO3I            )   ! OH+RNO3I=HO2+NO2+...
     &     +    1.9000D-01 * RKI( NCELL, 839 ) * YC( NCELL, IMAPAN           )   ! OH+IMAPAN=...

      R5_4  = R5_4 * DTC

c..Production of HO2 from HNO4 (r5,21 term )
      R5_21 =                 RKI( NCELL,  33 )                            ! HNO4=HO2+NO2
     &      +    6.1000D-01 * RKI( NCELL,  34 )                            ! HNO4=0.6100D+...

      R5_21 = R5_21 * DTC

c..Remaining HO2 production terms
      P5 =                 RXRAT( NCELL,  50 )      ! MEO2+NO=HO2+HCHO+NO2
     &   +                 RXRAT( NCELL,  53 )      ! MEO2+NO3=HO2+HCHO+NO2
     &   +    2.0000D+00 * RXRAT( NCELL,  55 )      ! MEO2+MEO2=0.2000D+01*HO2+...
     &   +    5.0000D-01 * RXRAT( NCELL,  59 )      ! RO2C+MEO2=0.5000D+00*HO2+...
     &   +    5.0000D-01 * RXRAT( NCELL,  64 )      ! RO2XC+MEO2=0.5000D+00*HO2+...
     &   +    9.0000D-01 * RXRAT( NCELL,  73 )      ! MECO3+MEO2=0.9000D+00*HO2+...
     &   +                 RXRAT( NCELL,  83 )      ! RCO3+MEO2=HO2+HCHO+RO2C+...
     &   +                 RXRAT( NCELL,  94 )      ! BZCO3+MEO2=HO2+HCHO+RO2C+...
     &   +                 RXRAT( NCELL, 108 )      ! xHO2+NO=HO2+NO
     &   +                 RXRAT( NCELL, 110 )      ! xHO2+NO3=HO2+NO3
     &   +    5.0000D-01 * RXRAT( NCELL, 111 )      ! xHO2+MEO2=0.5000D+00*HO2+MEO2
     &   +    5.0000D-01 * RXRAT( NCELL, 112 )      ! xHO2+RO2C=0.5000D+00*HO2+RO2C
     &   +    5.0000D-01 * RXRAT( NCELL, 113 )      ! xHO2+RO2XC=0.5000D+00*HO2+...
     &   +                 RXRAT( NCELL, 114 )      ! xHO2+MECO3=HO2+MECO3
     &   +                 RXRAT( NCELL, 115 )      ! xHO2+RCO3=HO2+RCO3
     &   +                 RXRAT( NCELL, 116 )      ! xHO2+BZCO3=HO2+BZCO3
     &   +                 RXRAT( NCELL, 117 )      ! xHO2+MACO3=HO2+MACO3
     &   +    2.0000D+00 * RXRAT( NCELL, 198 )      ! HCHO=0.2000D+01*HO2+CO
     &   +                 RXRAT( NCELL, 201 )      ! HCHO+NO3=HO2+HNO3+CO
     &   +                 RXRAT( NCELL, 203 )      ! CCHO=HO2+CO+MEO2
     &   +                 RXRAT( NCELL, 206 )      ! RCHO=HO2+xHO2+yROOH+xCCHO+...
     &   +                 RXRAT( NCELL, 217 )      ! COOH=HO2+OH+HCHO
     &   +                 RXRAT( NCELL, 219 )      ! ROOH=HO2+OH+RCHO
     &   +    1.4200D-01 * RXRAT( NCELL, 221 )      ! R6OOH=0.1420D+00*HO2+OH+...
     &   +                 RXRAT( NCELL, 223 )      ! RAOOH=HO2+OH+0.5000D+...
     &   +    2.0000D+00 * RXRAT( NCELL, 224 )      ! GLY=0.2000D+01*HO2+0.2000D+...
     &   +    6.3000D-01 * RXRAT( NCELL, 227 )      ! GLY+NO3=0.6300D+00*HO2+...
     &   +                 RXRAT( NCELL, 228 )      ! MGLY=HO2+CO+MECO3
     &   +    5.2200D-01 * RXRAT( NCELL, 241 )      ! AFG1+O3=0.5220D+00*HO2+...
     &   +    1.0230D+00 * RXRAT( NCELL, 242 )      ! AFG1=0.1023D+01*HO2+...
     &   +    5.2200D-01 * RXRAT( NCELL, 244 )      ! AFG2+O3=0.5220D+00*HO2+...
     &   +    5.5400D-01 * RXRAT( NCELL, 247 )      ! AFG3+O3=0.5540D+00*HO2+...
     &   +    1.0800D-01 * RXRAT( NCELL, 248 )      ! MACR+O3=0.1080D+00*HO2+...
     &   +    6.4000D-02 * RXRAT( NCELL, 250 )      ! MVK+O3=0.6400D-01*HO2+...
     &   +    4.0000D-01 * RXRAT( NCELL, 254 )      ! IPRD+O3=0.4000D+00*HO2+...
     &   +    1.2330D+00 * RXRAT( NCELL, 256 )      ! IPRD=0.1233D+01*HO2+...
     &   +    3.4400D-01 * RXRAT( NCELL, 260 )      ! RNO3=0.3440D+00*HO2+...
     &   +    2.0000D+00 * RXRAT( NCELL, 261 )      ! HOCCHO=0.2000D+01*HO2+CO+HCHO
     &   +    8.3000D-01 * RXRAT( NCELL, 264 )      ! ACROLEIN+O3=0.8300D+00*HO2+...
     &   +    1.0660D+00 * RXRAT( NCELL, 267 )      ! ACROLEIN=0.1066D+01*HO2+...
     &   +                 RXRAT( NCELL, 474 )      ! zRNO3+NO3=HO2+PRD2+NO3
     &   +    5.0000D-01 * RXRAT( NCELL, 475 )      ! zRNO3+MEO2=0.5000D+00*HO2+...
     &   +    5.0000D-01 * RXRAT( NCELL, 476 )      ! zRNO3+RO2C=0.5000D+00*HO2+...
     &   +    5.0000D-01 * RXRAT( NCELL, 477 )      ! zRNO3+RO2XC=0.5000D+00*HO2+...
     &   +                 RXRAT( NCELL, 478 )      ! zRNO3+MECO3=HO2+PRD2+MECO3
     &   +                 RXRAT( NCELL, 479 )      ! zRNO3+RCO3=HO2+PRD2+RCO3
     &   +                 RXRAT( NCELL, 480 )      ! zRNO3+BZCO3=HO2+PRD2+BZCO3
     &   +                 RXRAT( NCELL, 481 )      ! zRNO3+MACO3=HO2+PRD2+MACO3
     &   +    1.6000D-01 * RXRAT( NCELL, 504 )      ! ETHENE+O3=0.1600D+00*HO2+...
     &   +    8.0000D-01 * RXRAT( NCELL, 506 )      ! ETHENE+O3P=0.8000D+00*HO2+...
     &   +    1.6500D-01 * RXRAT( NCELL, 508 )      ! PROPENE+O3=0.1650D+00*HO2+...
     &   +    8.0000D-02 * RXRAT( NCELL, 512 )      ! BUTADIENE13+O3=0.8000D-...
     &   +    2.5000D-01 * RXRAT( NCELL, 514 )      ! BUTADIENE13+O3P=0.2500D+...
     &   +    6.6000D-02 * RXRAT( NCELL, 515 )      ! ISOPRENE+O3=0.6600D-01*HO2+...
     &   +    9.0000D-03 * RXRAT( NCELL, 518 )      ! APIN+O3=0.9000D-02*HO2+...
     &   +    1.5000D+00 * RXRAT( NCELL, 522 )      ! ACETYLENE+O3=0.1500D+...
     &   +    9.5000D-02 * RXRAT( NCELL, 536 )      ! OLE1+O3=0.9500D-01*HO2+...
     &   +    9.4000D-02 * RXRAT( NCELL, 540 )      ! OLE2+O3=0.9400D-01*HO2+...
     &   +    7.8000D-02 * RXRAT( NCELL, 546 )      ! TERP+O3=0.7800D-01*HO2+...
     &   +    7.8000D-02 * RXRAT( NCELL, 550 )      ! SESQ+O3=0.7800D-01*HO2+...
     &   +                 RXRAT( NCELL, 574 )      ! CL=HO2+HCL
     &   +                 RXRAT( NCELL, 575 )      ! HCHO+CL=HO2+HCL+CO
