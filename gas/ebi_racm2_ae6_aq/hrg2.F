     &   +    1.0000D-01 * RXRAT( NCELL, 131 )      ! API+O3=0.1000D+00*HO2+...
     &   +    1.0000D-01 * RXRAT( NCELL, 132 )      ! LIM+O3=0.1000D+00*HO2+...
     &   +    1.4000D-01 * RXRAT( NCELL, 133 )      ! MACR+O3=0.1400D+00*HO2+...
     &   +    1.1000D-01 * RXRAT( NCELL, 134 )      ! MVK+O3=0.1100D+00*HO2+...
     &   +    7.2000D-02 * RXRAT( NCELL, 135 )      ! UALD+O3=0.7200D-01*HO2+...
     &   +                 RXRAT( NCELL, 136 )      ! DCB1+O3=HO2+0.5000D-01*HO+...
     &   +                 RXRAT( NCELL, 137 )      ! DCB2+O3=HO2+0.5000D-01*HO+...
     &   +                 RXRAT( NCELL, 138 )      ! DCB3+O3=HO2+0.5000D-01*HO+...
