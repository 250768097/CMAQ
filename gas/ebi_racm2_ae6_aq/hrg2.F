     &   +    1.5000D+00 * RXRAT( NCELL,  25 )      ! DCB1=0.1500D+01*HO2+...
     &   +    1.5000D+00 * RXRAT( NCELL,  26 )      ! DCB2=0.1500D+01*HO2+...
     &   +                 RXRAT( NCELL,  27 )      ! BALD=HO2+CHO+CO
     &   +                 RXRAT( NCELL,  28 )      ! OP1=HO2+HO+HCHO
     &   +                 RXRAT( NCELL,  29 )      ! OP2=HO2+HO+ALD
     &   +                 RXRAT( NCELL,  31 )      ! ONIT=HO2+NO2+0.2000D+...
     &   +    1.5000D-01 * RXRAT( NCELL, 126 )      ! ETE+O3=0.1500D+00*HO2+...
     &   +    3.2000D-01 * RXRAT( NCELL, 127 )      ! OLT+O3=0.3200D+00*HO2+...
     &   +    7.0000D-02 * RXRAT( NCELL, 128 )      ! OLI+O3=0.7000D-01*HO2+...
     &   +    3.0000D-01 * RXRAT( NCELL, 129 )      ! DIEN+O3=0.3000D+00*HO2+...
