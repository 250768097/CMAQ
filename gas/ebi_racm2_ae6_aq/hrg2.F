     &   +                 RXRAT( NCELL,  12 )      ! ACD=HO2+MO2+CO
     &   +                 RXRAT( NCELL,  13 )      ! ALD=HO2+ETHP+CO
     &   +    1.2200D+00 * RXRAT( NCELL,  15 )      ! UALD=0.1220D+01*HO2+...
     &   +                 RXRAT( NCELL,  18 )      ! HKET=HO2+ACO3+HCHO
     &   +    6.6000D-01 * RXRAT( NCELL,  19 )      ! MACR=0.6600D+00*HO2+...
