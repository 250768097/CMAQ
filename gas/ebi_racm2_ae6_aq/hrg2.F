     &     +                 RKI( NCELL, 107 ) * YC( NCELL, EPX     )   ! HO+EPX=HO2+XO2+...
     &     +                 RKI( NCELL, 109 ) * YC( NCELL, MOH     )   ! HO+MOH=HO2+HCHO
     &     +                 RKI( NCELL, 110 ) * YC( NCELL, EOH     )   ! HO+EOH=HO2+ACD
     &     +                 RKI( NCELL, 111 ) * YC( NCELL, ROH     )   ! HO+ROH=HO2+...
     &     +                 RKI( NCELL, 112 ) * YC( NCELL, ETEG    )   ! HO+ETEG=HO2+ALD
     &     +                 RKI( NCELL, 117 ) * YC( NCELL, ORA1    )   ! HO+ORA1=HO2

      R5_4  = R5_4 * DTC

c..Production of HO2 from HNO4 (r5,21 term )
