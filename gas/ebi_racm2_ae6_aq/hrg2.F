     &     +                 RKI( NCELL,  43 )                   ! HO=HO2
     &     +                 RKI( NCELL,  47 ) * YC( NCELL, H2O2    )   ! HO+H2O2=HO2
     &     +                 RKI( NCELL,  58 ) * YC( NCELL, NO3     )   ! HO+NO3=HO2+NO2
     &     +                 RKI( NCELL,  69 ) * YC( NCELL, SO2     )   ! HO+SO2=HO2+SULF+...
     &     +                 RKI( NCELL,  70 ) * YC( NCELL, CO      )   ! HO+CO=HO2
     &     +    4.9000D-02 * RKI( NCELL,  75 ) * YC( NCELL, HC8     )   ! HO+HC8=0.4900D-...
