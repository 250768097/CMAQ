     &   +    2.5000D-01 * RXRAT( NCELL, 130 )      ! ISO+O3=0.2500D+00*HO+...
     &   +    8.5000D-01 * RXRAT( NCELL, 131 )      ! API+O3=0.8500D+00*HO+...
     &   +    8.5000D-01 * RXRAT( NCELL, 132 )      ! LIM+O3=0.8500D+00*HO+...
     &   +    1.9000D-01 * RXRAT( NCELL, 133 )      ! MACR+O3=0.1900D+00*HO+...
     &   +    1.6000D-01 * RXRAT( NCELL, 134 )      ! MVK+O3=0.1600D+00*HO+...
     &   +    1.0000D-01 * RXRAT( NCELL, 135 )      ! UALD+O3=0.1000D+00*HO+...
     &   +    5.0000D-02 * RXRAT( NCELL, 136 )      ! DCB1+O3=0.5000D-01*HO+HO2+...
     &   +    5.0000D-02 * RXRAT( NCELL, 137 )      ! DCB2+O3=0.5000D-01*HO+HO2+...
     &   +    5.0000D-02 * RXRAT( NCELL, 138 )      ! DCB3+O3=0.5000D-01*HO+HO2+...
     &   +    5.0000D-02 * RXRAT( NCELL, 139 )      ! EPX+O3=0.5000D-01*HO+...
     &   +    5.0000D-01 * RXRAT( NCELL, 157 )      ! EPX+NO3=0.5000D+00*HO+...
     &   +    2.8000D-01 * RXRAT( NCELL, 160 )      ! TR2=0.2800D+00*HO+0.2900D+...
     &   +    4.9000D-01 * RXRAT( NCELL, 161 )      ! TOLP=0.4900D+00*HO+0.1000D-...
     &   +    1.5800D-01 * RXRAT( NCELL, 162 )      ! XY2=0.1580D+00*HO+0.3080D+...
     &   +    3.9000D-01 * RXRAT( NCELL, 163 )      ! XYLP=0.3900D+00*HO+0.1000D-...
     &   +    1.5800D-01 * RXRAT( NCELL, 164 )      ! XYO2=0.1580D+00*HO+0.3080D+...
     &   +    3.9000D-01 * RXRAT( NCELL, 165 )      ! XYOP=0.3900D+00*HO+0.1000D-...

      P4 = YC0( NCELL, HO ) + P4 * DTC

c..Production of HO2 from OH ( r5,4 terms )
