C what(1) key, module and SID; SCCS file; date and time of last delta:
C %W% %P% %G% %U%

      SUBROUTINE HRG2( DTC )
C**********************************************************************
C
C  FUNCTION: To solve for the concentration of HO, HO2, HONO, and
C            HNO4 alebraically.
C
C  PRECONDITIONS: For the SAPRC07TB family of mechanisms
C
C  KEY SUBROUTINES/FUNCTIONS CALLED: None
C
C  REVISION HISTORY: Created by EBI solver program, July 18, 2014
C
C   18 Jul 14 B.Hutzell: revised to use real(8) variables
C**********************************************************************
      USE HRDATA

      IMPLICIT NONE


C..INCLUDES: None


C..ARGUMENTS:
      REAL( 8 ), INTENT( IN ) :: DTC                      ! Time step

C..PARAMETERS: None


C..EXTERNAL FUNCTIONS: NONE


C..SAVED LOCAL VARIABLES:
      CHARACTER( 16 ), SAVE :: PNAME = 'HRG2'             ! Program name


C..SCRATCH LOCAL VARIABLES:
      REAL( 8 ) ::    O1D_S                 ! sum of O1D loss frequencies
      REAL( 8 ) ::    OH_S                  ! stoich coeff for OH from O1D+H2O or H2
      REAL( 8 ) ::    HO2_S                 ! stoich coeff for HO2 from O1D+H2 rxn
      REAL( 8 ) ::    EXN_S                 ! sum of NO2EX loss frequencies
      REAL( 8 ) ::    XOH_S                 ! stoich coeff for OH & HONO from NO2EX loss rxn
      REAL( 8 ) ::    R4_19                 ! production of OH from HONO
      REAL( 8 ) ::    R19_4                 ! production of HONO from OH
      REAL( 8 ) ::    R4_5                  ! production of OH from HO2
      REAL( 8 ) ::    R5_4                  ! production of HO2 from OH
      REAL( 8 ) ::    R5_21                 ! production of HO2 from HNO4
      REAL( 8 ) ::    R21_5                 ! production of HNO4 from HO2
      REAL( 8 ) ::    P4, P5, P19, P21      ! species production form other paths
      REAL( 8 ) ::    L4, L5, L19, L21      ! species loss terms

      REAL( 8 ) ::    A, B, C               ! coeffs of quadratic eq. for HO2
      REAL( 8 ) ::    Q                     ! intermediate term

      REAL( 8 ) ::    T1, T2, T3            ! intermediate terms

      REAL( 8 ) ::    L21_INV               ! reciprocal of HNO4 loss term

C**********************************************************************


c..stoichiometric coefficient for production of HOX from O3 via O1D
      O1D_S =                 RKI( NCELL,  20 )                            ! O1D=0.2000D+01*OH
     &      +                 RKI( NCELL,  21 )                            ! O1D=O3P

      OH_S =    2.0000D+00 * RKI( NCELL,  20 )                            ! O1D=0.2000D+01*OH

      OH_S  = OH_S / O1D_S


c..Production of HO from HO2 (r4,5 terms )
      R4_5 =                 RKI( NCELL,  31 ) * YC( NCELL, NO               )   ! HO2+NO=OH+NO2
     &     +                 RKI( NCELL,  36 ) * YC( NCELL, O3               )   ! HO2+O3=OH
     &     +    8.0000D-01 * RKI( NCELL,  39 ) * YC( NCELL, NO3              )   ! HO2+NO3=0.8000D+...
     &     +                 RKI( NCELL, 326 ) * YC( NCELL, CL               )   ! HO2+CL=OH+CLO

      R4_5  = R4_5 * DTC

c..Production of HO from HONO (r4,19 terms )
      R4_19 =                 RKI( NCELL,  23 )                            ! HONO=OH+NO

      R4_19 = R4_19 * DTC

c..Remaining HO production
      P4 =   OH_S  *       RXRAT( NCELL,  18 )      ! O1D=0.2000D+01*OH
     &   +                 RXRAT( NCELL,  28 )      ! HNO3=OH+NO2
     &   +    3.9000D-01 * RXRAT( NCELL,  34 )      ! HNO4=0.3900D+00*OH+0.6100D+...
     &   +    2.0000D+00 * RXRAT( NCELL,  41 )      ! HO2H=0.2000D+01*OH
     &   +                 RXRAT( NCELL,  49 )      ! NO2EX=OH+HONO
     &   +                 RXRAT( NCELL, 120 )      ! xOH=OH
     &   +                 RXRAT( NCELL, 155 )      ! COOH=OH+HO2+HCHO
     &   +                 RXRAT( NCELL, 157 )      ! ROOH=OH+HO2+RCHO
     &   +                 RXRAT( NCELL, 159 )      ! R6OOH=OH+0.1420D+00*HO2+...
     &   +                 RXRAT( NCELL, 161 )      ! RAOOH=OH+HO2+0.5000D+...
     &   +    8.2600D-01 * RXRAT( NCELL, 179 )      ! AFG1+O3=0.8260D+00*OH+...
     &   +    8.2600D-01 * RXRAT( NCELL, 182 )      ! AFG2+O3=0.8260D+00*OH+...
     &   +    4.7100D-01 * RXRAT( NCELL, 185 )      ! AFG3+O3=0.4710D+00*OH+...
     &   +    2.0800D-01 * RXRAT( NCELL, 187 )      ! MACR+O3=0.2080D+00*OH+...
     &   +    3.3000D-01 * RXRAT( NCELL, 190 )      ! MACR=0.3300D+00*OH+0.6700D+...
     &   +    1.6400D-01 * RXRAT( NCELL, 192 )      ! MVK+O3=0.1640D+00*OH+...
     &   +    2.8500D-01 * RXRAT( NCELL, 196 )      ! IPRD+O3=0.2850D+00*OH+...
     &   +    3.3000D-01 * RXRAT( NCELL, 207 )      ! ACROLEIN+O3=0.3300D+00*OH+...
     &   +    1.7800D-01 * RXRAT( NCELL, 210 )      ! ACROLEIN=0.1780D+00*OH+...
     &   +                 RXRAT( NCELL, 212 )      ! CCOOOH=OH+CO2+MEO2
     &   +                 RXRAT( NCELL, 214 )      ! RCOOOH=OH+xCCHO+yROOH+CO2+xHO2
     &   +    1.6000D-01 * RXRAT( NCELL, 267 )      ! ETHENE+O3=0.1600D+00*OH+...
     &   +    3.5000D-01 * RXRAT( NCELL, 271 )      ! PROPENE+O3=0.3500D+00*OH+...
     &   +    8.0000D-02 * RXRAT( NCELL, 275 )      ! BUTADIENE13+O3=0.8000D-...
     &   +    2.6600D-01 * RXRAT( NCELL, 279 )      ! ISOPRENE+O3=0.2660D+00*OH+...
     &   +    7.2800D-01 * RXRAT( NCELL, 283 )      ! APIN+O3=0.7280D+00*OH+...
     &   +    5.0000D-01 * RXRAT( NCELL, 287 )      ! ACETYLENE+O3=0.5000D+00*OH+...
     &   +    1.2800D-01 * RXRAT( NCELL, 301 )      ! OLE1+O3=0.1280D+00*OH+...
     &   +    4.4300D-01 * RXRAT( NCELL, 305 )      ! OLE2+O3=0.4430D+00*OH+...
     &   +    4.9900D-01 * RXRAT( NCELL, 311 )      ! TERP+O3=0.4990D+00*OH+...
     &   +    4.9900D-01 * RXRAT( NCELL, 315 )      ! SESQ+O3=0.4990D+00*OH+...
     &   +                 RXRAT( NCELL, 336 )      ! HOCL=OH+CL
     &   +    4.1400D-01 * RXRAT( NCELL, 352 )      ! ROOH+CL=0.4140D+00*OH+HCL+...
     &   +    1.4500D-01 * RXRAT( NCELL, 353 )      ! R6OOH+CL=0.1450D+00*OH+HCL+...
     &   +    1.3900D-01 * RXRAT( NCELL, 354 )      ! RAOOH+CL=0.1390D+00*OH+...

      P4 = YC0( NCELL, OH ) + P4 * DTC

c..Production of HO2 from OH ( r5,4 terms )
      R5_4 =                 RKI( NCELL,  26 ) * YC( NCELL, NO3              )   ! OH+NO3=HO2+NO2
     &     +                 RKI( NCELL,  29 ) * YC( NCELL, CO               )   ! OH+CO=HO2+CO2
     &     +                 RKI( NCELL,  30 ) * YC( NCELL, O3               )   ! OH+O3=HO2
     &     +                 RKI( NCELL,  42 ) * YC( NCELL, HO2H             )   ! OH+HO2H=HO2
     &     +                 RKI( NCELL,  44 ) * YC( NCELL, SO2              )   ! OH+SO2=HO2+SULF+...
     &     +                 RKI( NCELL,  45 )                            ! OH=HO2
     &     +                 RKI( NCELL, 138 ) * YC( NCELL, HCHO             )   ! OH+HCHO=HO2+CO
     &     +                 RKI( NCELL, 150 ) * YC( NCELL, MEOH             )   ! OH+MEOH=HO2+HCHO
     &     +                 RKI( NCELL, 151 ) * YC( NCELL, HCOOH            )   ! OH+HCOOH=HO2+CO2
     &     +    1.4800D-01 * RKI( NCELL, 160 ) * YC( NCELL, RAOOH            )   ! OH+RAOOH=0.1480D+...
     &     +    6.3000D-01 * RKI( NCELL, 164 ) * YC( NCELL, GLY              )   ! OH+GLY=0.6300D+...
     &     +    4.7200D-01 * RKI( NCELL, 199 ) * YC( NCELL, PRD2             )   ! OH+PRD2=0.4720D+...
     &     +    1.8900D-01 * RKI( NCELL, 201 ) * YC( NCELL, RNO3             )   ! OH+RNO3=0.1890D+...
     &     +    3.0000D-01 * RKI( NCELL, 286 ) * YC( NCELL, ACETYLENE        )   ! OH+ACETYLENE=...
     &     +    5.7000D-01 * RKI( NCELL, 288 ) * YC( NCELL, BENZENE          )   ! OH+BENZENE=...
     &     +    1.8100D-01 * RKI( NCELL, 289 ) * YC( NCELL, TOLUENE          )   ! OH+TOLUENE=...
     &     +    1.5900D-01 * RKI( NCELL, 290 ) * YC( NCELL, MXYL             )   ! OH+MXYL=0.1590D+...
     &     +    1.6100D-01 * RKI( NCELL, 291 ) * YC( NCELL, OXYL             )   ! OH+OXYL=0.1610D+...
     &     +    1.5900D-01 * RKI( NCELL, 292 ) * YC( NCELL, PXYL             )   ! OH+PXYL=0.1590D+...
     &     +    2.2000D-02 * RKI( NCELL, 293 ) * YC( NCELL, TRIMETH_BENZ124  )   ! OH+...
     &     +    9.5000D-01 * RKI( NCELL, 294 ) * YC( NCELL, ETOH             )   ! OH+ETOH=0.9500D+...
     &     +    1.2300D-01 * RKI( NCELL, 308 ) * YC( NCELL, ARO1             )   ! OH+ARO1=0.1230D+...
     &     +    7.7000D-02 * RKI( NCELL, 309 ) * YC( NCELL, ARO2             )   ! OH+ARO2=0.7700D-...

      R5_4  = R5_4 * DTC

c..Production of HO2 from HNO4 (r5,21 term )
      R5_21 =                 RKI( NCELL,  33 )                            ! HNO4=HO2+NO2
     &      +    6.1000D-01 * RKI( NCELL,  34 )                            ! HNO4=0.6100D+...

      R5_21 = R5_21 * DTC

c..Remaining HO2 production terms
      P5 =                 RXRAT( NCELL,  50 )      ! MEO2+NO=HO2+HCHO+NO2
     &   +                 RXRAT( NCELL,  53 )      ! MEO2+NO3=HO2+HCHO+NO2
     &   +    2.0000D+00 * RXRAT( NCELL,  55 )      ! MEO2+MEO2=0.2000D+01*HO2+...
     &   +    5.0000D-01 * RXRAT( NCELL,  59 )      ! RO2C+MEO2=0.5000D+00*HO2+...
     &   +    5.0000D-01 * RXRAT( NCELL,  64 )      ! RO2XC+MEO2=0.5000D+00*HO2+...
     &   +    9.0000D-01 * RXRAT( NCELL,  73 )      ! MECO3+MEO2=0.9000D+00*HO2+...
     &   +                 RXRAT( NCELL,  83 )      ! RCO3+MEO2=HO2+HCHO+RO2C+...
     &   +                 RXRAT( NCELL,  94 )      ! BZCO3+MEO2=HO2+HCHO+RO2C+...
     &   +                 RXRAT( NCELL, 106 )      ! MACO3+MEO2=HO2+0.2000D+...
     &   +                 RXRAT( NCELL, 118 )      ! xHO2=HO2
     &   +    2.0000D+00 * RXRAT( NCELL, 136 )      ! HCHO=0.2000D+01*HO2+CO
     &   +                 RXRAT( NCELL, 139 )      ! HCHO+NO3=HO2+HNO3+CO
     &   +                 RXRAT( NCELL, 141 )      ! CCHO=HO2+CO+MEO2
     &   +                 RXRAT( NCELL, 144 )      ! RCHO=HO2+xHO2+yROOH+xCCHO+...
     &   +                 RXRAT( NCELL, 155 )      ! COOH=HO2+OH+HCHO
     &   +                 RXRAT( NCELL, 157 )      ! ROOH=HO2+OH+RCHO
     &   +    1.4200D-01 * RXRAT( NCELL, 159 )      ! R6OOH=0.1420D+00*HO2+OH+...
     &   +                 RXRAT( NCELL, 161 )      ! RAOOH=HO2+OH+0.5000D+...
     &   +    2.0000D+00 * RXRAT( NCELL, 162 )      ! GLY=0.2000D+01*HO2+0.2000D+...
     &   +    6.3000D-01 * RXRAT( NCELL, 165 )      ! GLY+NO3=0.6300D+00*HO2+...
     &   +                 RXRAT( NCELL, 166 )      ! MGLY=HO2+CO+MECO3
     &   +    5.2200D-01 * RXRAT( NCELL, 179 )      ! AFG1+O3=0.5220D+00*HO2+...
     &   +    1.0230D+00 * RXRAT( NCELL, 180 )      ! AFG1=0.1023D+01*HO2+...
     &   +    5.2200D-01 * RXRAT( NCELL, 182 )      ! AFG2+O3=0.5220D+00*HO2+...
     &   +    5.5400D-01 * RXRAT( NCELL, 185 )      ! AFG3+O3=0.5540D+00*HO2+...
     &   +    1.0800D-01 * RXRAT( NCELL, 187 )      ! MACR+O3=0.1080D+00*HO2+...
     &   +    6.7000D-01 * RXRAT( NCELL, 190 )      ! MACR=0.6700D+00*HO2+...
     &   +    6.4000D-02 * RXRAT( NCELL, 192 )      ! MVK+O3=0.6400D-01*HO2+...
     &   +    4.0000D-01 * RXRAT( NCELL, 196 )      ! IPRD+O3=0.4000D+00*HO2+...
     &   +    1.2330D+00 * RXRAT( NCELL, 198 )      ! IPRD=0.1233D+01*HO2+...
     &   +    3.4400D-01 * RXRAT( NCELL, 202 )      ! RNO3=0.3440D+00*HO2+...
     &   +    2.0000D+00 * RXRAT( NCELL, 204 )      ! HOCCHO=0.2000D+01*HO2+CO+HCHO
     &   +    8.3000D-01 * RXRAT( NCELL, 207 )      ! ACROLEIN+O3=0.8300D+00*HO2+...
     &   +    1.0660D+00 * RXRAT( NCELL, 210 )      ! ACROLEIN=0.1066D+01*HO2+...
     &   +                 RXRAT( NCELL, 250 )      ! zRNO3=HO2+PRD2
     &   +    1.6000D-01 * RXRAT( NCELL, 267 )      ! ETHENE+O3=0.1600D+00*HO2+...
     &   +    8.0000D-01 * RXRAT( NCELL, 269 )      ! ETHENE+O3P=0.8000D+00*HO2+...
     &   +    1.6500D-01 * RXRAT( NCELL, 271 )      ! PROPENE+O3=0.1650D+00*HO2+...
     &   +    8.0000D-02 * RXRAT( NCELL, 275 )      ! BUTADIENE13+O3=0.8000D-...
     &   +    2.5000D-01 * RXRAT( NCELL, 277 )      ! BUTADIENE13+O3P=0.2500D+...
     &   +    6.6000D-02 * RXRAT( NCELL, 279 )      ! ISOPRENE+O3=0.6600D-01*HO2+...
     &   +    9.0000D-03 * RXRAT( NCELL, 283 )      ! APIN+O3=0.9000D-02*HO2+...
     &   +    1.5000D+00 * RXRAT( NCELL, 287 )      ! ACETYLENE+O3=0.1500D+...
     &   +    9.5000D-02 * RXRAT( NCELL, 301 )      ! OLE1+O3=0.9500D-01*HO2+...
     &   +    9.4000D-02 * RXRAT( NCELL, 305 )      ! OLE2+O3=0.9400D-01*HO2+...
     &   +    7.8000D-02 * RXRAT( NCELL, 311 )      ! TERP+O3=0.7800D-01*HO2+...
     &   +    7.8000D-02 * RXRAT( NCELL, 315 )      ! SESQ+O3=0.7800D-01*HO2+...
     &   +                 RXRAT( NCELL, 339 )      ! CL=HO2+HCL
     &   +                 RXRAT( NCELL, 340 )      ! HCHO+CL=HO2+HCL+CO
     &   +                 RXRAT( NCELL, 342 )      ! MEOH+CL=HO2+HCHO+HCL
     &   +    5.5000D-02 * RXRAT( NCELL, 346 )      ! RNO3+CL=0.5500D-01*HO2+...
     &   +    3.1400D-01 * RXRAT( NCELL, 347 )      ! PRD2+CL=0.3140D+00*HO2+HCL+...
     &   +    6.3000D-01 * RXRAT( NCELL, 348 )      ! GLY+CL=0.6300D+00*HO2+HCL+...
     &   +    1.4800D-01 * RXRAT( NCELL, 354 )      ! RAOOH+CL=0.1480D+00*HO2+...
     &   +    8.4000D-02 * RXRAT( NCELL, 358 )      ! IPRD+CL=0.8400D-01*HO2+...
     &   +                 RXRAT( NCELL, 359 )      ! CLCCHO=HO2+CO+RO2C+xCL+...
     &   +                 RXRAT( NCELL, 375 )      ! ACETYLENE+CL=HO2+CO
     &   +    6.8800D-01 * RXRAT( NCELL, 381 )      ! ETOH+CL=0.6880D+00*HO2+HCL+...

      P5 = YC0( NCELL, HO2 ) + P5 * DTC

c..Production of HONO from OH (r19,4 terms )

      R19_4 =   RKI( NCELL,  22 ) * YC( NCELL, NO               ) * DTC      ! OH+NO=HONO

c..Remaining HONO production terms
      P19 =                 RXRAT( NCELL,  49 )      ! NO2EX=HONO+OH
     &    +                 RXRAT( NCELL, 173 )      ! NPHE=HONO
     &    +    5.0000D-01 * RXRAT( NCELL, 415 )      ! NO2=0.5000D+00*HONO+...

      P19 = YC0( NCELL, HONO ) + P19 * DTC

c..Production of HNO4 from HO2 (r21,5 term )

      R21_5 =   RKI( NCELL,  32 ) * YC( NCELL, NO2              ) * DTC      ! HO2+NO2=HNO4

c..Remaining HNO4 production terms
      P21   =   YC0( NCELL, HNO4 )

c..OH loss terms not in R5_4 & R19_4
      L4 =                 RKI( NCELL,  24 ) * YC( NCELL, HONO             )   ! OH+HONO=NO2
     &   +                 RKI( NCELL,  25 ) * YC( NCELL, NO2              )   ! OH+NO2=HNO3
     &   +                 RKI( NCELL,  27 ) * YC( NCELL, HNO3             )   ! OH+HNO3=NO3
     &   +                 RKI( NCELL,  35 ) * YC( NCELL, HNO4             )   ! OH+HNO4=NO2
     &   +                 RKI( NCELL,  43 ) * YC( NCELL, HO2              )   ! OH+HO2=
     &   +                 RKI( NCELL, 140 ) * YC( NCELL, CCHO             )   ! OH+CCHO=MECO3
     &   +                 RKI( NCELL, 143 ) * YC( NCELL, RCHO             )   ! OH+RCHO=0.9650D+...
     &   +                 RKI( NCELL, 146 ) * YC( NCELL, ACETONE          )   ! OH+ACETONE=RO2C+...
     &   +                 RKI( NCELL, 148 ) * YC( NCELL, MEK              )   ! OH+MEK=0.9670D+...
     &   +                 RKI( NCELL, 152 ) * YC( NCELL, CCOOH            )   ! OH+CCOOH=0.5090D+...
     &   +                 RKI( NCELL, 153 ) * YC( NCELL, RCOOH            )   ! OH+RCOOH=RO2C+...
     &   +    7.0000D-01 * RKI( NCELL, 154 ) * YC( NCELL, COOH             )   ! OH+COOH=0.3000D+...
     &   +    2.5600D-01 * RKI( NCELL, 156 ) * YC( NCELL, ROOH             )   ! OH+ROOH=0.7440D+...
     &   +    1.6000D-01 * RKI( NCELL, 158 ) * YC( NCELL, R6OOH            )   ! OH+R6OOH=0.8400D+...
     &   +    7.1300D-01 * RKI( NCELL, 160 ) * YC( NCELL, RAOOH            )   ! OH+RAOOH=0.1480D+...
     &   +    3.7000D-01 * RKI( NCELL, 164 ) * YC( NCELL, GLY              )   ! OH+GLY=0.6300D+...
     &   +                 RKI( NCELL, 167 ) * YC( NCELL, MGLY             )   ! OH+MGLY=CO+MECO3
     &   +                 RKI( NCELL, 170 ) * YC( NCELL, CRES             )   ! OH+CRES=0.2000D+...
     &   +                 RKI( NCELL, 172 ) * YC( NCELL, NPHE             )   ! OH+NPHE=BZO
     &   +                 RKI( NCELL, 175 ) * YC( NCELL, BALD             )   ! OH+BALD=BZCO3
     &   +                 RKI( NCELL, 178 ) * YC( NCELL, AFG1             )   ! OH+AFG1=0.2170D+...
     &   +                 RKI( NCELL, 181 ) * YC( NCELL, AFG2             )   ! OH+AFG2=0.2170D+...
     &   +                 RKI( NCELL, 184 ) * YC( NCELL, AFG3             )   ! OH+AFG3=0.2060D+...
     &   +                 RKI( NCELL, 186 ) * YC( NCELL, MACR             )   ! OH+MACR=0.5000D+...
     &   +                 RKI( NCELL, 191 ) * YC( NCELL, MVK              )   ! OH+MVK=0.9750D+...
     &   +                 RKI( NCELL, 195 ) * YC( NCELL, IPRD             )   ! OH+IPRD=0.2890D+...
     &   +    5.2800D-01 * RKI( NCELL, 199 ) * YC( NCELL, PRD2             )   ! OH+PRD2=0.4720D+...
     &   +    8.1100D-01 * RKI( NCELL, 201 ) * YC( NCELL, RNO3             )   ! OH+RNO3=0.1890D+...
     &   +                 RKI( NCELL, 203 ) * YC( NCELL, HOCCHO           )   ! OH+HOCCHO=MECO3
     &   +                 RKI( NCELL, 206 ) * YC( NCELL, ACROLEIN         )   ! OH+ACROLEIN=...
     &   +                 RKI( NCELL, 211 ) * YC( NCELL, CCOOOH           )   ! OH+CCOOOH=...
     &   +                 RKI( NCELL, 213 ) * YC( NCELL, RCOOOH           )   ! OH+RCOOOH=...
     &   +                 RKI( NCELL, 265 )                            ! OH=MEO2
     &   +                 RKI( NCELL, 266 ) * YC( NCELL, ETHENE           )   ! OH+ETHENE=xHO2+...
     &   +                 RKI( NCELL, 270 ) * YC( NCELL, PROPENE          )   ! OH+PROPENE=...
     &   +                 RKI( NCELL, 274 ) * YC( NCELL, BUTADIENE13      )   ! OH+BUTADIENE13=...
     &   +                 RKI( NCELL, 278 ) * YC( NCELL, ISOPRENE         )   ! OH+ISOPRENE=...
     &   +                 RKI( NCELL, 282 ) * YC( NCELL, APIN             )   ! OH+APIN=0.7990D+...
     &   +    3.1400D-01 * RKI( NCELL, 288 ) * YC( NCELL, BENZENE          )   ! OH+BENZENE=...
     &   +    5.0700D-01 * RKI( NCELL, 289 ) * YC( NCELL, TOLUENE          )   ! OH+TOLUENE=...
     &   +    6.0200D-01 * RKI( NCELL, 290 ) * YC( NCELL, MXYL             )   ! OH+MXYL=0.1590D+...
     &   +    6.4100D-01 * RKI( NCELL, 291 ) * YC( NCELL, OXYL             )   ! OH+OXYL=0.1610D+...
     &   +    5.6300D-01 * RKI( NCELL, 292 ) * YC( NCELL, PXYL             )   ! OH+PXYL=0.1590D+...
     &   +    7.4800D-01 * RKI( NCELL, 293 ) * YC( NCELL, TRIMETH_BENZ124  )   ! OH+...
     &   +    5.0000D-02 * RKI( NCELL, 294 ) * YC( NCELL, ETOH             )   ! OH+ETOH=0.9500D+...
     &   +                 RKI( NCELL, 295 ) * YC( NCELL, ALK1             )   ! OH+ALK1=xHO2+...
     &   +                 RKI( NCELL, 296 ) * YC( NCELL, ALK2             )   ! OH+ALK2=0.9650D+...
     &   +                 RKI( NCELL, 297 ) * YC( NCELL, ALK3             )   ! OH+ALK3=0.6950D+...
     &   +                 RKI( NCELL, 298 ) * YC( NCELL, ALK4             )   ! OH+ALK4=0.8300D+...
     &   +                 RKI( NCELL, 299 ) * YC( NCELL, ALK5             )   ! OH+ALK5=0.6470D+...
     &   +                 RKI( NCELL, 300 ) * YC( NCELL, OLE1             )   ! OH+OLE1=0.8710D+...
     &   +                 RKI( NCELL, 304 ) * YC( NCELL, OLE2             )   ! OH+OLE2=0.9120D+...
     &   +    6.7500D-01 * RKI( NCELL, 308 ) * YC( NCELL, ARO1             )   ! OH+ARO1=0.1230D+...
     &   +    7.4500D-01 * RKI( NCELL, 309 ) * YC( NCELL, ARO2             )   ! OH+ARO2=0.7700D-...
     &   +                 RKI( NCELL, 310 ) * YC( NCELL, TERP             )   ! OH+TERP=0.7340D+...
     &   +                 RKI( NCELL, 314 ) * YC( NCELL, SESQ             )   ! OH+SESQ=0.7340D+...
     &   +                 RKI( NCELL, 338 ) * YC( NCELL, HCL              )   ! OH+HCL=CL
     &   +                 RKI( NCELL, 360 ) * YC( NCELL, CLCCHO           )   ! OH+CLCCHO=RCO3

      L4    = 1.0D0 + L4 * DTC + R5_4 + R19_4

c..HO2 loss terms not included in R4_5 & R21_5 (except for HO2+HO2 )
      L5 =    2.0000D-01 * RKI( NCELL,  39 ) * YC( NCELL, NO3              )   ! HO2+NO3=0.8000D+...
     &   +                 RKI( NCELL,  43 ) * YC( NCELL, OH               )   ! HO2+OH=
     &   +                 RKI( NCELL,  51 ) * YC( NCELL, MEO2             )   ! HO2+MEO2=COOH
     &   +                 RKI( NCELL,  52 ) * YC( NCELL, MEO2             )   ! HO2+MEO2=HCHO
     &   +                 RKI( NCELL,  57 ) * YC( NCELL, RO2C             )   ! HO2+RO2C=
     &   +                 RKI( NCELL,  62 ) * YC( NCELL, RO2XC            )   ! HO2+RO2XC=
     &   +                 RKI( NCELL,  71 ) * YC( NCELL, MECO3            )   ! HO2+MECO3=...
     &   +                 RKI( NCELL,  81 ) * YC( NCELL, RCO3             )   ! HO2+RCO3=0.7500D+...
     &   +                 RKI( NCELL,  92 ) * YC( NCELL, BZCO3            )   ! HO2+BZCO3=...
     &   +                 RKI( NCELL, 104 ) * YC( NCELL, MACO3            )   ! HO2+MACO3=...
     &   +                 RKI( NCELL, 116 ) * YC( NCELL, BZO              )   ! HO2+BZO=CRES
     &   +                 RKI( NCELL, 325 ) * YC( NCELL, CL               )   ! HO2+CL=HCL
     &   +                 RKI( NCELL, 335 ) * YC( NCELL, CLO              )   ! HO2+CLO=HOCL

      L5    = 1.0D0 + L5 * DTC + R4_5 + R21_5

c..HONO loss terms not included in R4_19
      L19 =                 RKI( NCELL,  24 ) * YC( NCELL, OH               )   ! HONO+OH=NO2

      L19   = 1.0D0 + L19 * DTC + R4_19

c..HNO4 loss terms not inluded in R5_21
      L21 =    3.9000D-01 * RKI( NCELL,  34 )                            ! HNO4=0.6100D+...
     &    +                 RKI( NCELL,  35 ) * YC( NCELL, OH               )   ! HNO4+OH=NO2

      L21   = 1.0D0 + L21 * DTC + R5_21

ccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccc
c  Solution section
ccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccccc

c..compute terms used to calculate a,b & c
      L21_INV = 1.0D0 / L21
      T1 = 1.0D0 / ( L4 * L19 - R4_19 * R19_4 )
      T2 = R5_4 * T1
      T3 = R5_21 * L21_INV

c..solve quadratic equation for HO2
      A = 2.0D0 * ( RKI( NCELL,  37 ) + RKI( NCELL,  38 ) ) * DTC

      B = L5 - T3 * R21_5 - T2 * R4_5 * L19

      C = P5 + T3 * P21 + T2 * ( P4 * L19 + P19 * R4_19 )

      Q = -0.5D0 * ( B + SIGN( 1.0D0, B ) * SQRT( B * B + 4.0D0 * A * C ) )

      YCP( NCELL, HO2 ) = MAX( Q / A , -C / Q  )

c..compute remaining species concentrations
      YCP( NCELL, OH ) = ( ( P4 + R4_5 * YCP( NCELL, HO2 ) ) * L19 + R4_19 * P19 ) * T1

      YCP( NCELL, HNO4 ) = ( P21 + R21_5 * YCP( NCELL, HO2 ) ) * L21_INV

      YCP( NCELL, HONO ) = ( P19 + R19_4 * YCP( NCELL, OH ) ) / L19

      RETURN

      END


